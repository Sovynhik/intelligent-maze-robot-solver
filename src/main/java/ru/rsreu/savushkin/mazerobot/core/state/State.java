package ru.rsreu.savushkin.mazerobot.core.state;

/**
 * Универсальный интерфейс состояния системы (State).
 * <p>Представляет собой снимок всех переменных, необходимых для однозначного
 * определения текущего положения в пространстве состояний.</p>
 * <p>Все реализации этого интерфейса должны корректно определять методы {@code equals}
 * и {@code hashCode} для правильной работы алгоритмов поиска.</p>
 */
public interface State {
    /**
     * Сравнивает текущее состояние с другим объектом.
     * <p>Необходимо для алгоритмов поиска (например, BFS, A*) при проверке посещенных состояний.</p>
     * @param obj Объект для сравнения.
     * @return {@code true}, если состояния равны.
     */
    @Override
    boolean equals(Object obj);

    /**
     * Возвращает хэш-код состояния.
     * <p>Необходимо для корректной работы состояний в коллекциях {@code Set} и {@code Map}.</p>
     * @return Целочисленное значение хэш-кода.
     */
    @Override
    int hashCode();

    /**
     * Возвращает строковое представление состояния.
     * @return Строка, описывающая состояние.
     */
    @Override
    String toString();
}
package ru.rsreu.savushkin.mazerobot.core.entity;

/**
 * Типы ячеек, составляющих поле лабиринта.
 * Определяет, может ли робот пройти через ячейку и является ли она целью.
 */
public enum CellType {
    /** Пустая ячейка, по которой робот может перемещаться. */
    EMPTY,
    /** Непроходимая стена. */
    WALL,
    /** Целевая ячейка (Клад). */
    TREASURE
}